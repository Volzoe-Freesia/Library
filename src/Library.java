
public class Library {
	private int l_Id;
	private String l_Address;
	private String l_Area;
	private String l_Tel;
	public int getL_Id() {
		return l_Id;
	}
	public void setL_Id(int l_Id) {
		this.l_Id = l_Id;
	}
	public String getL_Address() {
		return l_Address;
	}
	public void setL_Address(String l_Address) {
		this.l_Address = l_Address;
	}
	public String getL_Area() {
		return l_Area;
	}
	public void setL_Area(String l_Area) {
		this.l_Area = l_Area;
	}
	public String getL_Tel() {
		return l_Tel;
	}
	public void setL_Tel(String l_Tel) {
		this.l_Tel = l_Tel;
	}
	
}
